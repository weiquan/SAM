import sys
import wgsim
import art
usage = '''***********************************************************************
Usage:          aln_eval.py     <cmd>   [opt]

Cmd:            wgsim           reads are generated by wgism
                art             reads are generated by art   
***********************************************************************'''

UHIT = 0 #un hit
PHIT = 1 #perfect hit
GHIT = 2 #good hit
BHIT = 3 #bad hit
FLAG_UNMAP = 0x4
FLAG_REVERSE = 0x10

if __name__ == '__main__':
    if len(sys.argv) < 2:
        print >>sys.stderr, usage;
        exit(1)
    cmd = sys.argv[1]
    if(cmd == 'wgsim'):
        wgsim.wgsim_alnEval(sys.argv[1:])
    elif(cmd == 'art'):
        art.art_alnEval(sys.argv[1:])
    else:
        print >>sys.stderr, 'no cmd called '+cmd
        print >>sys.stderr, usage;



